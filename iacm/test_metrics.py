from iacm.metrics import get_distr_xy


def test_get_distr_xy():
    p = dict()
    p['00000'] = 0.2
    p['00001'] = 0.0
    p['00010'] = 0.1
    p['00011'] = 0.1
    p['00100'] = 0.0
    p['00101'] = 0.0
    p['00110'] = 0.1
    p['00111'] = 0.0
    p['01000'] = 0.0
    p['01001'] = 0.0
    p['01010'] = 0.0
    p['01011'] = 0.0
    p['01100'] = 0.0
    p['01101'] = 0.0
    p['01110'] = 0.0
    p['01111'] = 0.1
    p['10000'] = 0.0
    p['10001'] = 0.1
    p['10010'] = 0.0
    p['10011'] = 0.0
    p['10100'] = 0.0
    p['10101'] = 0.0
    p['10110'] = 0.1
    p['10111'] = 0.0
    p['11000'] = 0.0
    p['11001'] = 0.0
    p['11010'] = 0.0
    p['11011'] = 0.1
    p['11100'] = 0.0
    p['11101'] = 0.0
    p['11110'] = 0.1
    p['11111'] = 0.0

    pxy = get_distr_xy(p, 2, 2)
    assert pxy[0] == 0.5
    assert pxy[1] == 0.1
    assert pxy[2] == 0.2
    assert pxy[3] == 0.2
